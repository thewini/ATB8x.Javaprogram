package Exercises.ex_20092024.If_else_Condition;

public class If_else_Ex3 {
    public static void main(String[] args) {
        //Example3
        boolean b=!true;          //b=false
        if(b) {                   //if(false) executes else condition
            System.out.println("b is true");
        }
        else {
            System.out.println("b is not true");
        }
    }
}
