package Exercises.ex_11092024;

public class println_vs_print {
    public static void main(String[] args)
    {
        //difference btw println() and print()
        System.out.println("HI ASHWINI");
        System.out.println("HI VIKAS");     //println() will print in seperate line

        System.out.print("Hello world ");
        System.out.print("HOW ARE YOU");   //print() will print in single line
    }
}
